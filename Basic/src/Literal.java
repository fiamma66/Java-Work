
public class Literal {

	public static void main(String[] args) {
		int n=100;
		System.out.println(n);
		n=0144; //8進制
		System.out.println(n);
		n=0x64; //16進制
		System.out.println(n); //印出仍為10進制
		
		double d = 3.14159;
		System.out.println(d);
		d = 3.14e5;
		System.out.println(d);
		d = 3.14e-2;
		System.out.println(d);
		String s = "special"; //'S'tring 大寫
		System.out.println(s);
		char c = 'A';
		System.out.println(c); //字元只能1字
		c = 36;
		System.out.println(c); //ASCII 內碼 ASCII Table
		c = '\u0043';
		System.out.println(c);
		
		
		
		
		
				
		

	}

}
