
public class EscapeSequence {

	public static void main(String[] args) {
		System.out.println("Hello World!");
		System.out.print("Hello Word!\n" );
		System.out.print("Hello\nWorld\n");
		System.out.print("Hello");
		System.out.print("World!\n");
		System.out.print("\tSpecial!\n");
		System.out.print("This is Pan's pen!");
		System.out.print("\nThis is Pan\'s pen!"); //可加可不加\
		System.out.print("\nI say:\"I don't want it!\""); //印出"一定要加\

	}

}
